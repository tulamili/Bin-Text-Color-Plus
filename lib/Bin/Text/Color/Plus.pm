Bin::Text::Color::Plus

=head1 SYNOPSIS

This module "Bin::Text::Color::Plus" provides scripts for specific functions.

=head1 DESCRIPTION

The included commands are as follows. 

=over 4

=item *
1. B<colorplus> ; Useful functions. First try "colorplus --help".  

=item *
2. B<resistorcc> ; Put colors on numerical digits (0 to 9) according to the electric resistance color codes. Somewaht arranged. 1 for brown, 2 for red, 3 for orange and so on. 

=item *
3. B<backcolor> ; Put blue colors with the 6 brightness degrees on the numbers. Note: not so matured functions yet, sorry!  

=back

